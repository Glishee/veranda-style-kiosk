export function normalizeProductSlug(slug?: string | null) {
    if (!slug) return ''

    const value = slug.toLowerCase()

    if (value.includes('solid')) return 'solid'
    if (value.includes('bold')) return 'bold'
    if (value.includes('cube-grand') || value.includes('cube_grand') || value.includes('grand')) return 'cube_grand'
    if (value.includes('cube')) return 'cube'
    if (value.includes('carport')) return 'carport'
    if (value.includes('prime')) return 'prime'
    if (value.includes('dynamic')) return 'dynamic'
    if (value.includes('advanced')) return 'advanced'
    if (value.includes('adaptive')) return 'adaptive'

    if (
        value.includes('sun') ||
        value.includes('shading') ||
        value.includes('przeciwsloneczne') ||
        value.includes('przeciwsłoneczne')
    ) {
        return 'sun_shading'
    }

    return value
}

export function getPricingTable(productSlug?: string | null) {
    return PRICING_TABLES[normalizeProductSlug(productSlug)] ?? null
}

export function calculatePricePln(eur: number) {
    return Math.round(eur * PRICE_MULTIPLIER * EUR_TO_PLN)
}

export function getPriceEur(table: PricingTable, width: number, depth: number) {
    const widthIndex = table.widths.indexOf(width)
    const depthIndex = table.depths.indexOf(depth)

    if (widthIndex === -1 || depthIndex === -1) return null

    return table.prices[depthIndex]?.[widthIndex] ?? null
}